package Strings;

public class CompareString {
    public static void main(String[] args) {
        String name1 = "Shree";
        String name2 = "Karan";
        if (name1.compareTo(name2) == 0) {
            System.out.println("Strings are equal");
        } else {
            System.out.println("Strings are not equal");
        }
    }

}
